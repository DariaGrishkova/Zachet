Select Distinct Номера.ID_номера as [Занятые номера],
                dbo.Поселение.Дата_поселения as [Дата поселения],
				dbo.Поселение.Датa_выселения as [Дата выселения]
from dbo.Поселение, dbo.Номера
where dbo.Поселение.ID_номера = dbo.Номера.ID_номера and
      dbo.Поселение.Датa_выселения < CONVERT(Date, GETDATE())